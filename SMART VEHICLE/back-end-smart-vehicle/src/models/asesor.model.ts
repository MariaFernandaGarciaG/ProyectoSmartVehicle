import {Entity, model, property, hasOne, hasMany} from '@loopback/repository';
import {Usuario} from './usuario.model';
import {Cliente} from './cliente.model';
import {Producto} from './producto.model';

@model()
export class Asesor extends Entity {
  @property({
    type: 'string',
    id: true,
    generated: true,
  })
  id?: string;

  @property({
    type: 'string',
    required: true,
  })
  identificacion: string;

  @property({
    type: 'string',
    required: true,
  })
  nombre: string;

  @property({
    type: 'string',
    required: true,
  })
  apellido: string;

  @property({
    type: 'string',
    required: true,
  })
  telefono: string;

  @property({
    type: 'string',
    required: true,
  })
  direccion: string;

  @property({
    type: 'string',
    required: true,
  })
  email: string;

  @hasOne(() => Usuario)
  usuario: Usuario;

  @hasMany(() => Cliente)
  clientes: Cliente[];

  @hasMany(() => Producto)
  productos: Producto[];

  constructor(data?: Partial<Asesor>) {
    super(data);
  }
}

export interface AsesorRelations {
  // describe navigational properties here
}

export type AsesorWithRelations = Asesor & AsesorRelations;
